void draw_line(uint32_t line, uint8_t *line_buf) {
	if(line == 0 || line == ILI9341_HEIGHT - 1){
		for(unsigned i = 0; i < 2 * ILI9341_WIDTH; i += 2) line_buf[i] = (ILI9341_WHITE >> 8) & 0xFF, line_buf[i + 1] = ILI9341_WHITE & 0xFF;
		return;
	}

	uint16_t _intensity = (line % 32 * 8 + intensity) % 256;
	uint16_t color;

	if(line / 32 % 3 == 0) color = ILI9341_COLOR565(_intensity, 0, 0);
	else if(line / 32 % 3 == 1) color = ILI9341_COLOR565(0, _intensity, 0);
	else color = ILI9341_COLOR565(0, 0, _intensity);

	for(unsigned i = 0; i < 2 * ILI9341_WIDTH; i += 2) line_buf[i] = (color >> 8) & 0xFF, line_buf[i + 1] = color & 0xFF;

	line_buf[0                    ] = (ILI9341_WHITE >> 8) & 0xFF, line_buf[1                   ] = ILI9341_WHITE & 0xFF;
	line_buf[ILI9341_WIDTH * 2 - 2] = (ILI9341_WHITE >> 8) & 0xFF, line_buf[ILI9341_WIDTH * 2 -1] = ILI9341_WHITE & 0xFF;
}

